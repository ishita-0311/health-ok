import os
import sys

import streamlit as st

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
from style import hero, inject, risk_badge  # noqa: E402
from utils import DISEASES, load_schema, load_model_and_scaler, predict_proba  # noqa: E402

st.set_page_config(page_title="Health-OK | Prediction", page_icon="⚕", layout="wide")
inject()

hero(
    "Disease Risk Prediction",
    "Pick a disease, enter clinical values, and get a statistical risk estimate from "
    "the best-performing trained model.",
    eyebrow="Prediction",
)

st.warning(
    "**Disclaimer:** This tool provides an automated statistical estimate only. "
    "It is **not** a medical diagnosis. Please consult a qualified healthcare "
    "professional for any health concerns."
)

disease_key = st.selectbox(
    "Choose a disease",
    options=list(DISEASES.keys()),
    format_func=lambda k: DISEASES[k]["title"],
)
info = DISEASES[disease_key]

try:
    schema = load_schema(disease_key)
except FileNotFoundError:
    st.error("Model not trained yet. Run `python train/train_all.py` first.")
    st.stop()

st.caption(f"Serving model: **{schema['winner']}**")

st.subheader(f"Enter {info['title']} risk factors")
values = {}
cols = st.columns(2)
for i, feat in enumerate(schema["features"]):
    with cols[i % 2]:
        fmin, fmax = float(feat["min"]), float(feat["max"])
        is_binary = fmin == 0.0 and fmax == 1.0
        is_small_int_range = fmax > fmin and fmax - fmin <= 5 and float(fmin).is_integer() and float(fmax).is_integer()

        if is_binary:
            default_idx = 1 if round(feat["default"]) == 1 else 0
            choice = st.selectbox(
                feat["label"],
                options=[0, 1],
                index=default_idx,
                format_func=lambda v: "Yes (1)" if v == 1 else "No (0)",
                key=f"{disease_key}_{feat['name']}",
            )
            values[feat["name"]] = float(choice)
        elif is_small_int_range:
            options = list(range(int(fmin), int(fmax) + 1))
            default_val = min(options, key=lambda o: abs(o - feat["default"]))
            values[feat["name"]] = float(
                st.selectbox(
                    feat["label"],
                    options=options,
                    index=options.index(default_val),
                    key=f"{disease_key}_{feat['name']}",
                )
            )
        else:
            step = (fmax - fmin) / 100 or 0.1
            values[feat["name"]] = st.number_input(
                feat["label"],
                min_value=fmin,
                max_value=fmax if fmax > fmin else fmin + 1,
                value=float(feat["default"]),
                step=float(step) if step > 0 else 1.0,
                key=f"{disease_key}_{feat['name']}",
            )

if st.button("Predict", type="primary"):
    model, scaler = load_model_and_scaler(disease_key, schema)
    proba = predict_proba(disease_key, schema, model, scaler, values)

    pct = proba * 100
    if proba < 0.33:
        risk_label = "Low Risk"
    elif proba < 0.66:
        risk_label = "Medium Risk"
    else:
        risk_label = "High Risk"

    st.markdown(
        f"""
        <div class="hok-result">
            <div style="font-size:0.95rem; opacity:0.75; font-weight:600;">PREDICTED RISK, {info['title'].upper()}</div>
            <div class="hok-pct">{pct:.1f}%</div>
            {risk_badge(risk_label)}
        </div>
        """,
        unsafe_allow_html=True,
    )
    st.progress(min(max(proba, 0.0), 1.0))

    st.info(
        "This estimate is generated by a machine learning model trained on a "
        "reference dataset and reflects statistical patterns only. It is **not** "
        "a clinical diagnosis. Consult a doctor for professional medical advice."
    )
